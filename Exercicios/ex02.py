# Tipos Primitivos : Int, Float, Bool, Str
# Int: 1 / 25 / 5 / 34
# Float: 1.0 / 6.33 / 5.0 / 12.23
# String: "1.0" / "Sei la" / ""
# Bool: True / False

n1 = int(input("Digite o Primeiro Numero: "))
